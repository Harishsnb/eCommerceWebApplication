exports.getInvoice = (req, res, next) => {
  const orderId = req.params.orderId;
  Order.findById(orderId)
    .then((order) => {
      if (!order) {
        return next(new Error("order not found"));
      }
      if (order.user.userId.toString() !== req.user._id.toString()) {
        return next(new Error("Not authorized"));
      }
      let totalCost = 0;
      const invName = `invoice- + ${orderId} + .pdf`;
      const invPath = path.join("data", "invoice", invName);
      const pdfFile = new PDFDocument();
      res.setHeader("Content-Type", "application/pdf");
      res.setHeader(
        "Content-Disposition",
        "inline; filename = `+ ${invName} +`"
      );
      pdfFile.pipe(fs.createWriteStream(invPath));
      pdfFile.pipe(res);
      pdfFile.fontSize(20).text(`Invoice for order : ${orderId}`);
      pdfFile.text(
        `----------------------------------------------------------------`
      );
      order.products.forEach((p) => {
        totalCost += p.quantity * p.product.price;
        //console.log(totalCost);
        pdfFile
          .fontSize(12)
          .text(`${p.product.title} - ${p.quantity} x $ ${p.product.price}`);
      });
      pdfFile.text(
        `----------------------------------------------------------------`
      );
      pdfFile.text(`Total Price: $ ${totalCost}`);
      pdfFile.end();
    })
    .catch((err) => next(err));
};

